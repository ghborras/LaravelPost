     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $comment = new Comment;
        $comment->content_com = $request->content_com;
        $comment->user_id = Auth::id();
        $comment->post_id = $id;
        $post = Post::find($id);
        $id = $post->user->id;
        $comment->save();
        return redirect()->action('BlogsController@show', $id);

    }

